public class ClassMethodsInfo {

    private int methodsCount;

    private ClassMethodInfo[] methodInfos;

    public int getMethodsCount() {
        return methodsCount;
    }

    public void setMethodsCount(int methodsCount) {
        this.methodsCount = methodsCount;
    }

    public ClassMethodInfo[] getMethodInfos() {
        return methodInfos;
    }

    public void setMethodInfos(ClassMethodInfo[] methodInfos) {
        this.methodInfos = methodInfos;
    }
}
